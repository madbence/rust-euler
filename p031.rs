fn main() {
    let mut c = 0;
    for p200 in 0..up(200, 200) {
        for p100 in 0..up(200 - p200 * 200, 100) {
            for p50 in 0..up(200 - p200 * 200 - p100 * 100, 50) {
                for p20 in 0..up(200 - p200 * 200 - p100 * 100 - p50 * 50, 20) {
                    for p10 in 0..up(200 - p200 * 200 - p100 * 100 - p50 * 50 - p20 * 20, 10) {
                        for p5 in 0..up(200 - p200 * 200 - p100 * 100 - p50 * 50 - p20 * 20 - p10 * 10, 5) {
                            for p2 in 0..up(200 - p200 * 200 - p100 * 100 - p50 * 50 - p20 * 20 - p10 * 10 - p5 * 5, 2) {
                                let p1 = 200 - p200 * 200 - p100 * 100 - p50 * 50 - p20 * 20 - p10 * 10 - p5 * 5 - p2 * 2;
                                // println!("{}x200 + {}x100 + {}x50 + {}x20 + {}x10 + {}x5 + {}x2 + {}x1", p200, p100, p50, p20, p10, p5, p2, p1);
                                c += 1;
                            }
                        }
                    }
                }
            }
        }
    }
    println!("{}", c);
}

fn up(base: u32, size: u32) -> u32 {
    base / size + 1
}
